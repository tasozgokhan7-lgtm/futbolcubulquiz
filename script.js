/* ===== Language handling ===== */
const langToggle = document.getElementById('langToggle');

function applyLang(lang) {
  const dict = I18N[lang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });
  // Translatable placeholders and tooltips (used in the demo game)
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (dict[key] !== undefined) el.placeholder = dict[key];
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    if (dict[key] !== undefined) el.title = dict[key];
  });
  // Bilingual content blocks (used on privacy / support pages)
  document.querySelectorAll('[data-lang]').forEach(el => {
    el.hidden = el.getAttribute('data-lang') !== lang;
  });
  document.documentElement.lang = lang;
  // Notify listeners (e.g. the demo game) AFTER the lang is set so they read the new value
  document.dispatchEvent(new CustomEvent('langchange', { detail: { lang } }));
  // toggle button highlight
  langToggle.querySelector('.lang-tr').classList.toggle('active', lang === 'tr');
  langToggle.querySelector('.lang-en').classList.toggle('active', lang === 'en');
  try { localStorage.setItem('fb_lang', lang); } catch (e) {}
}

let currentLang = 'tr';
try {
  const saved = localStorage.getItem('fb_lang');
  if (saved === 'en' || saved === 'tr') currentLang = saved;
  else if ((navigator.language || '').toLowerCase().startsWith('en')) currentLang = 'en';
} catch (e) {}

applyLang(currentLang);

langToggle.addEventListener('click', () => {
  currentLang = currentLang === 'tr' ? 'en' : 'tr';
  applyLang(currentLang);
});

